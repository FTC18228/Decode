package org.firstinspires.ftc.teamcode.util;

public class JoystickValues {
    public double leftX;
    public double leftY;
    public double rightX;
    public double rightY;
    public JoystickValues(double leftX, double leftY, double rightX, double rightY) {
        this.leftX = leftX;
        this.leftY = leftY;
        this.rightX = rightX;
        this.rightY = rightY;
    }
}
